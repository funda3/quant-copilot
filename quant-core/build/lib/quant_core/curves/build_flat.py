"""
build_flat — Construct a flat-rate :class:`DiscountCurve`.

A flat curve assigns the same continuously-applied simple rate to every
pillar.  Discount factors are computed as::

    df(t) = 1 / (1 + rate * tau)

where *tau* is the year fraction from *valuation_date* to the pillar date
under the supplied day-count convention.

This is consistent with the V1 FastAPI pricer's pricing philosophy and is
used as the baseline curve for testing before a bootstrapped curve is
available.
"""
from __future__ import annotations

from datetime import date

from quant_core.conventions.day_count import DayCount, accrual_fraction
from quant_core.conventions.schedule import generate_unadjusted_dates
from quant_core.curves.discount_curve import DiscountCurve


def flat_curve(
    valuation_date: date,
    rate: float,
    tenor_years: int,
    frequency: str = "annual",
    day_count: DayCount = DayCount.ACT_365F,
) -> DiscountCurve:
    """
    Build a flat-rate :class:`DiscountCurve` from a single rate.

    Pillar dates are generated by :func:`~quant_core.conventions.schedule.\
generate_unadjusted_dates` from *valuation_date* out to *tenor_years* at the
    supplied *frequency*.  No business-day adjustment is applied.

    Parameters
    ----------
    valuation_date : date
        Curve as-of date (df = 1 here by definition).
    rate : float
        Flat (simple) rate, e.g. 0.08 for 8%.  Must be > -1 (allows
        negative-rate curves as long as dfs remain positive).
    tenor_years : int
        Maximum tenor in whole years.  Must be >= 1.
    frequency : str, optional
        Coupon frequency used to generate pillar dates.  Must be one of
        ``"monthly"``, ``"quarterly"``, ``"semiannual"``, ``"annual"``.
        Defaults to ``"annual"``.
    day_count : DayCount, optional
        Day-count convention for year-fraction computation.  Defaults to
        :attr:`DayCount.ACT_365F`.

    Returns
    -------
    DiscountCurve
        A fully constructed :class:`DiscountCurve`.

    Raises
    ------
    ValueError
        For invalid *rate*, *tenor_years*, or *frequency* arguments.
    """
    if rate <= -1.0:
        raise ValueError(
            f"rate must be > -1 so that discount factors remain positive; "
            f"got {rate}"
        )

    pillar_dates = generate_unadjusted_dates(valuation_date, tenor_years, frequency)

    discount_factors: list[float] = []
    for d in pillar_dates:
        tau = accrual_fraction(valuation_date, d, day_count)
        df = 1.0 / (1.0 + rate * tau)
        discount_factors.append(df)

    return DiscountCurve(valuation_date, pillar_dates, discount_factors)
